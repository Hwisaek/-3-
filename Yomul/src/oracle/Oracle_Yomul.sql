FROM (
SELECT CHAT_FROM, MAX(CDATE) CDATE
FROM ( SELECT CHAT_TO AS CHAT_FROM, CDATE
          FROM YOMUL_CHATS
          WHERE CHAT_FROM = 'M1'
          UNION
          SELECT CHAT_FROM AS CHAT_FROM, CDATE FROM YOMUL_CHATS WHERE CHAT_TO = 'M1')
GROUP BY CHAT_FROM) C
JOIN YOMUL_MEMBERS M ON C.CHAT_FROM = M.NO
ORDER BY CDATE DESC;

